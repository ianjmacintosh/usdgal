const getCurrencyByCountry = (countryCode: string) => {
  const country = countryData.find(
    (country) => country.CountryIsoCode === countryCode,
  );
  return country?.Currencies[0] ?? "USD";
};

const getUnitsByCountry = (countryCode: string): Units => {
  let units = "liter";

  if (countriesUsingGallons.includes(countryCode)) {
    units = "gallon";
  }

  return <Units>units;
};

